 <h3>Forståelse af stress</h3>
 <p>Stress er en universel realitet, der kan påvirke os alle, uanset om det er udløst af arbejdspres, personlige anliggender eller en kombination af begge. Det opstår, når der er en ubalance mellem vores ressourcer og de krav, vi står over for, eller når vi føler, at vores ressourcer ikke er tilstrækkelige. Stress er dybt personligt og påvirker os alle forskelligt. Det, der stresser din kollega, stresser ikke nødvendigvis dig, og omvendt.</p>

